package Pojos;

public class ViewCardStatement 
{
	
	private String  branchId;
	private String  cardRefNumber;
	private String  count;
	private String  period_Frdt;
	private String  period_todt;
	private String  txnType;
	

	public String getBranchId() {
		return branchId;
	}
	public void setBranchId(String branchId) {
		this.branchId = branchId;
	}
	public String getCardRefNumber() {
		return cardRefNumber;
	}
	public void setCardRefNumber(String cardRefNumber) {
		this.cardRefNumber = cardRefNumber;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getPeriod_Frdt() {
		return period_Frdt;
	}
	public void setPeriod_Frdt(String period_Frdt) {
		this.period_Frdt = period_Frdt;
	}
	public String getPeriod_todt() {
		return period_todt;
	}
	public void setPeriod_todt(String period_todt) {
		this.period_todt = period_todt;
	}
	public String getTxnType() {
		return txnType;
	}
	public void setTxnType(String txnType) {
		this.txnType = txnType;
	}

	
}
