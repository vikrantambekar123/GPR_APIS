package Pojos;

public class ReplaceCardPojo 
{
	
	
private String	oldCardRefNumber;
private String	newCardRefNumber;
private String	mobileNumber;
private String	reason;
private String	paymentMode;
private String	txnRefNo;
private String	outletId;
	



public String getOldCardRefNumber() {
	return oldCardRefNumber;
}
public void setOldCardRefNumber(String oldCardRefNumber) {
	this.oldCardRefNumber = oldCardRefNumber;
}
public String getNewCardRefNumber() {
	return newCardRefNumber;
}
public void setNewCardRefNumber(String newCardRefNumber) {
	this.newCardRefNumber = newCardRefNumber;
}
public String getMobileNumber() {
	return mobileNumber;
}
public void setMobileNumber(String mobileNumber) {
	this.mobileNumber = mobileNumber;
}
public String getReason() {
	return reason;
}
public void setReason(String reason) {
	this.reason = reason;
}
public String getPaymentMode() {
	return paymentMode;
}
public void setPaymentMode(String paymentMode) {
	this.paymentMode = paymentMode;
}
public String getTxnRefNo() {
	return txnRefNo;
}
public void setTxnRefNo(String txnRefNo) {
	this.txnRefNo = txnRefNo;
}
public String getOutletId() {
	return outletId;
}
public void setOutletId(String outletId) {
	this.outletId = outletId;
}

}
